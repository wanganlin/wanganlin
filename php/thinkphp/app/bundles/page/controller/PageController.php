<?php

declare(strict_types=1);

namespace app\bundles\page\controller;

use app\api\admin\controller\BaseController;
use app\bundles\page\entity\PageEntity;
use app\bundles\page\request\page\PageCreateRequest;
use app\bundles\page\request\page\PageQueryRequest;
use app\bundles\page\request\page\PageUpdateRequest;
use app\bundles\page\response\page\PageQueryResponse;
use app\bundles\page\response\page\PageResponse;
use app\bundles\page\service\PageBundleService;
use Juling\Foundation\Exception\CustomException;
use OpenApi\Attributes as OA;
use think\facade\Db as DB;
use think\facade\Log;
use think\Response;
use Throwable;

class PageController extends BaseController
{
    #[OA\Post(path: '/page/query', summary: '查询列表接口', security: [['bearerAuth' => []]], tags: ['单页模块'])]
    #[OA\Parameter(name: 'page', description: '当前页码', in: 'query', required: true, example: 1)]
    #[OA\Parameter(name: 'pageSize', description: '每页分页数', in: 'query', required: false, example: 10)]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: PageQueryRequest::class))]
    #[OA\Response(response: 200, description: 'OK', content: new OA\JsonContent(ref: PageQueryResponse::class))]
    public function query(): Response
    {
        try {
            $request = $this->request->get();
            $page = intval($this->request->param('page', 1));
            $pageSize = intval($this->request->param('pageSize', 10));

            $v = new PageQueryRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $condition = [
                //
            ];

            $pageBundleService = new PageBundleService;
            $result = $pageBundleService->page($condition, $page, $pageSize);

            foreach ($result['data'] as $key => $item) {
                $response = new PageResponse;
                $response->loadData($item);
                $result['data'][$key] = $response->toArray();
            }

            return $this->success($result);
        } catch (Throwable $e) {
            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('查询列表错误');
        }
    }

    #[OA\Post(path: '/page/create', summary: '新增接口', security: [['bearerAuth' => []]], tags: ['单页模块'])]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: PageCreateRequest::class))]
    #[OA\Response(response: 200, description: 'OK')]
    public function create(): Response
    {
        DB::startTrans();
        try {
            $request = $this->request->get();

            $v = new PageCreateRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $pageEntity = new PageEntity;
            $pageEntity->loadData($request);

            $pageBundleService = new PageBundleService;
            $insertId = $pageBundleService->save($pageEntity->toEntity());
            if ($insertId > 0) {
                DB::commit();

                return $this->success('新增数据成功');
            }

            throw new CustomException('新增数据失败');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('新增数据错误');
        }
    }

    #[OA\Get(path: '/page/show', summary: '获取详情接口', security: [['bearerAuth' => []]], tags: ['单页模块'])]
    #[OA\Parameter(name: 'id', description: 'ID', in: 'query', required: true, example: 1)]
    #[OA\Response(response: 200, description: 'OK', content: new OA\JsonContent(ref: PageResponse::class))]
    public function show(): Response
    {
        try {
            $id = intval($this->request->param('id', 0));

            $condition = [
                ['id', '=', $id],
            ];

            $pageBundleService = new PageBundleService;
            $page = $pageBundleService->getOne($condition);

            if (empty($page)) {
                throw new CustomException('数据不存在或状态异常');
            }

            $response = new PageResponse;
            $response->loadData($page);

            return $this->success($response->toArray());
        } catch (Throwable $e) {
            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('获取详情错误');
        }
    }

    #[OA\Put(path: '/page/update', summary: '更新接口', security: [['bearerAuth' => []]], tags: ['单页模块'])]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: PageUpdateRequest::class))]
    #[OA\Response(response: 200, description: 'OK')]
    public function update(): Response
    {
        DB::startTrans();
        try {
            $request = $this->request->get();

            $v = new PageUpdateRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $pageBundleService = new PageBundleService;
            $page = $pageBundleService->getById($request['id']);
            if (empty($page)) {
                throw new CustomException('数据不存在或状态异常');
            }

            $pageEntity = new PageEntity;
            $pageEntity->loadData($request);

            $pageBundleService->update($pageEntity->toEntity(), [
                ['id', '=', $request['id']],
            ]);

            DB::commit();

            return $this->success('更新数据成功');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('更新数据错误');
        }
    }

    #[OA\Delete(path: '/page/destroy', summary: '删除接口', security: [['bearerAuth' => []]], tags: ['单页模块'])]
    #[OA\Parameter(name: 'id', description: 'ID', in: 'query', required: true, example: 1)]
    #[OA\Response(response: 200, description: 'OK')]
    public function destroy(): Response
    {
        DB::startTrans();
        try {
            $id = intval($this->request->param('id', 0));

            $condition = [
                ['id', '=', $id],
            ];

            $pageBundleService = new PageBundleService;
            if ($pageBundleService->remove($condition)) {
                DB::commit();

                return $this->success('删除数据成功');
            }

            throw new CustomException('删除数据失败');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('删除数据错误');
        }
    }
}
