<?php

declare(strict_types=1);

namespace app\bundles\dict\controller;

use app\api\admin\controller\BaseController;
use app\bundles\dict\entity\DictEntity;
use app\bundles\dict\request\dict\DictCreateRequest;
use app\bundles\dict\request\dict\DictQueryRequest;
use app\bundles\dict\request\dict\DictUpdateRequest;
use app\bundles\dict\response\dict\DictQueryResponse;
use app\bundles\dict\response\dict\DictResponse;
use app\bundles\dict\service\DictBundleService;
use Juling\Foundation\Exception\CustomException;
use OpenApi\Attributes as OA;
use think\facade\Db as DB;
use think\facade\Log;
use think\Response;
use Throwable;

class DictController extends BaseController
{
    #[OA\Post(path: '/dict/query', summary: '查询列表接口', security: [['bearerAuth' => []]], tags: ['字典模块'])]
    #[OA\Parameter(name: 'page', description: '当前页码', in: 'query', required: true, example: 1)]
    #[OA\Parameter(name: 'pageSize', description: '每页分页数', in: 'query', required: false, example: 10)]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: DictQueryRequest::class))]
    #[OA\Response(response: 200, description: 'OK', content: new OA\JsonContent(ref: DictQueryResponse::class))]
    public function query(): Response
    {
        try {
            $request = $this->request->get();
            $page = intval($this->request->param('page', 1));
            $pageSize = intval($this->request->param('pageSize', 10));

            $v = new DictQueryRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $condition = [
                //
            ];

            $dictBundleService = new DictBundleService;
            $result = $dictBundleService->page($condition, $page, $pageSize);

            foreach ($result['data'] as $key => $item) {
                $response = new DictResponse;
                $response->loadData($item);
                $result['data'][$key] = $response->toArray();
            }

            return $this->success($result);
        } catch (Throwable $e) {
            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('查询列表错误');
        }
    }

    #[OA\Post(path: '/dict/create', summary: '新增接口', security: [['bearerAuth' => []]], tags: ['字典模块'])]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: DictCreateRequest::class))]
    #[OA\Response(response: 200, description: 'OK')]
    public function create(): Response
    {
        DB::startTrans();
        try {
            $request = $this->request->get();

            $v = new DictCreateRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $dictEntity = new DictEntity;
            $dictEntity->loadData($request);

            $dictBundleService = new DictBundleService;
            $insertId = $dictBundleService->save($dictEntity->toEntity());
            if ($insertId > 0) {
                DB::commit();

                return $this->success('新增数据成功');
            }

            throw new CustomException('新增数据失败');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('新增数据错误');
        }
    }

    #[OA\Get(path: '/dict/show', summary: '获取详情接口', security: [['bearerAuth' => []]], tags: ['字典模块'])]
    #[OA\Parameter(name: 'id', description: 'ID', in: 'query', required: true, example: 1)]
    #[OA\Response(response: 200, description: 'OK', content: new OA\JsonContent(ref: DictResponse::class))]
    public function show(): Response
    {
        try {
            $id = intval($this->request->param('id', 0));

            $condition = [
                ['id', '=', $id],
            ];

            $dictBundleService = new DictBundleService;
            $dict = $dictBundleService->getOne($condition);

            if (empty($dict)) {
                throw new CustomException('数据不存在或状态异常');
            }

            $response = new DictResponse;
            $response->loadData($dict);

            return $this->success($response->toArray());
        } catch (Throwable $e) {
            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('获取详情错误');
        }
    }

    #[OA\Put(path: '/dict/update', summary: '更新接口', security: [['bearerAuth' => []]], tags: ['字典模块'])]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: DictUpdateRequest::class))]
    #[OA\Response(response: 200, description: 'OK')]
    public function update(): Response
    {
        DB::startTrans();
        try {
            $request = $this->request->get();

            $v = new DictUpdateRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $dictBundleService = new DictBundleService;
            $dict = $dictBundleService->getById($request['id']);
            if (empty($dict)) {
                throw new CustomException('数据不存在或状态异常');
            }

            $dictEntity = new DictEntity;
            $dictEntity->loadData($request);

            $dictBundleService->update($dictEntity->toEntity(), [
                ['id', '=', $request['id']],
            ]);

            DB::commit();

            return $this->success('更新数据成功');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('更新数据错误');
        }
    }

    #[OA\Delete(path: '/dict/destroy', summary: '删除接口', security: [['bearerAuth' => []]], tags: ['字典模块'])]
    #[OA\Parameter(name: 'id', description: 'ID', in: 'query', required: true, example: 1)]
    #[OA\Response(response: 200, description: 'OK')]
    public function destroy(): Response
    {
        DB::startTrans();
        try {
            $id = intval($this->request->param('id', 0));

            $condition = [
                ['id', '=', $id],
            ];

            $dictBundleService = new DictBundleService;
            if ($dictBundleService->remove($condition)) {
                DB::commit();

                return $this->success('删除数据成功');
            }

            throw new CustomException('删除数据失败');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('删除数据错误');
        }
    }
}
