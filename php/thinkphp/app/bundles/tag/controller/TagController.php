<?php

declare(strict_types=1);

namespace app\bundles\tag\controller;

use app\api\admin\controller\BaseController;
use app\bundles\tag\entity\TagEntity;
use app\bundles\tag\request\tag\TagCreateRequest;
use app\bundles\tag\request\tag\TagQueryRequest;
use app\bundles\tag\request\tag\TagUpdateRequest;
use app\bundles\tag\response\tag\TagQueryResponse;
use app\bundles\tag\response\tag\TagResponse;
use app\bundles\tag\service\TagBundleService;
use Juling\Foundation\Exception\CustomException;
use OpenApi\Attributes as OA;
use think\facade\Db as DB;
use think\facade\Log;
use think\Response;
use Throwable;

class TagController extends BaseController
{
    #[OA\Post(path: '/tag/query', summary: '查询列表接口', security: [['bearerAuth' => []]], tags: ['标签模块'])]
    #[OA\Parameter(name: 'page', description: '当前页码', in: 'query', required: true, example: 1)]
    #[OA\Parameter(name: 'pageSize', description: '每页分页数', in: 'query', required: false, example: 10)]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: TagQueryRequest::class))]
    #[OA\Response(response: 200, description: 'OK', content: new OA\JsonContent(ref: TagQueryResponse::class))]
    public function query(): Response
    {
        try {
            $request = $this->request->get();
            $page = intval($this->request->param('page', 1));
            $pageSize = intval($this->request->param('pageSize', 10));

            $v = new TagQueryRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $condition = [
                //
            ];

            $tagBundleService = new TagBundleService;
            $result = $tagBundleService->page($condition, $page, $pageSize);

            foreach ($result['data'] as $key => $item) {
                $response = new TagResponse;
                $response->loadData($item);
                $result['data'][$key] = $response->toArray();
            }

            return $this->success($result);
        } catch (Throwable $e) {
            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('查询列表错误');
        }
    }

    #[OA\Post(path: '/tag/create', summary: '新增接口', security: [['bearerAuth' => []]], tags: ['标签模块'])]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: TagCreateRequest::class))]
    #[OA\Response(response: 200, description: 'OK')]
    public function create(): Response
    {
        DB::startTrans();
        try {
            $request = $this->request->get();

            $v = new TagCreateRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $tagEntity = new TagEntity;
            $tagEntity->loadData($request);

            $tagBundleService = new TagBundleService;
            $insertId = $tagBundleService->save($tagEntity->toEntity());
            if ($insertId > 0) {
                DB::commit();

                return $this->success('新增数据成功');
            }

            throw new CustomException('新增数据失败');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('新增数据错误');
        }
    }

    #[OA\Get(path: '/tag/show', summary: '获取详情接口', security: [['bearerAuth' => []]], tags: ['标签模块'])]
    #[OA\Parameter(name: 'id', description: 'ID', in: 'query', required: true, example: 1)]
    #[OA\Response(response: 200, description: 'OK', content: new OA\JsonContent(ref: TagResponse::class))]
    public function show(): Response
    {
        try {
            $id = intval($this->request->param('id', 0));

            $condition = [
                ['id', '=', $id],
            ];

            $tagBundleService = new TagBundleService;
            $tag = $tagBundleService->getOne($condition);

            if (empty($tag)) {
                throw new CustomException('数据不存在或状态异常');
            }

            $response = new TagResponse;
            $response->loadData($tag);

            return $this->success($response->toArray());
        } catch (Throwable $e) {
            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('获取详情错误');
        }
    }

    #[OA\Put(path: '/tag/update', summary: '更新接口', security: [['bearerAuth' => []]], tags: ['标签模块'])]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: TagUpdateRequest::class))]
    #[OA\Response(response: 200, description: 'OK')]
    public function update(): Response
    {
        DB::startTrans();
        try {
            $request = $this->request->get();

            $v = new TagUpdateRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $tagBundleService = new TagBundleService;
            $tag = $tagBundleService->getById($request['id']);
            if (empty($tag)) {
                throw new CustomException('数据不存在或状态异常');
            }

            $tagEntity = new TagEntity;
            $tagEntity->loadData($request);

            $tagBundleService->update($tagEntity->toEntity(), [
                ['id', '=', $request['id']],
            ]);

            DB::commit();

            return $this->success('更新数据成功');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('更新数据错误');
        }
    }

    #[OA\Delete(path: '/tag/destroy', summary: '删除接口', security: [['bearerAuth' => []]], tags: ['标签模块'])]
    #[OA\Parameter(name: 'id', description: 'ID', in: 'query', required: true, example: 1)]
    #[OA\Response(response: 200, description: 'OK')]
    public function destroy(): Response
    {
        DB::startTrans();
        try {
            $id = intval($this->request->param('id', 0));

            $condition = [
                ['id', '=', $id],
            ];

            $tagBundleService = new TagBundleService;
            if ($tagBundleService->remove($condition)) {
                DB::commit();

                return $this->success('删除数据成功');
            }

            throw new CustomException('删除数据失败');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('删除数据错误');
        }
    }
}
