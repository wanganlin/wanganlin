<?php

declare(strict_types=1);

namespace app\bundles\article\controller;

use app\api\admin\controller\BaseController;
use app\bundles\article\entity\ArticleEntity;
use app\bundles\article\request\article\ArticleCreateRequest;
use app\bundles\article\request\article\ArticleQueryRequest;
use app\bundles\article\request\article\ArticleUpdateRequest;
use app\bundles\article\response\article\ArticleQueryResponse;
use app\bundles\article\response\article\ArticleResponse;
use app\bundles\article\service\ArticleBundleService;
use Juling\Foundation\Exception\CustomException;
use OpenApi\Attributes as OA;
use think\facade\Db as DB;
use think\facade\Log;
use think\Response;
use Throwable;

class ArticleController extends BaseController
{
    #[OA\Post(path: '/article/query', summary: '查询列表接口', security: [['bearerAuth' => []]], tags: ['文章模块'])]
    #[OA\Parameter(name: 'page', description: '当前页码', in: 'query', required: true, example: 1)]
    #[OA\Parameter(name: 'pageSize', description: '每页分页数', in: 'query', required: false, example: 10)]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: ArticleQueryRequest::class))]
    #[OA\Response(response: 200, description: 'OK', content: new OA\JsonContent(ref: ArticleQueryResponse::class))]
    public function query(): Response
    {
        try {
            $request = $this->request->get();
            $page = intval($this->request->param('page', 1));
            $pageSize = intval($this->request->param('pageSize', 10));

            $v = new ArticleQueryRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $condition = [
                //
            ];

            $articleBundleService = new ArticleBundleService;
            $result = $articleBundleService->page($condition, $page, $pageSize);

            foreach ($result['data'] as $key => $item) {
                $response = new ArticleResponse;
                $response->loadData($item);
                $result['data'][$key] = $response->toArray();
            }

            return $this->success($result);
        } catch (Throwable $e) {
            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('查询列表错误');
        }
    }

    #[OA\Post(path: '/article/create', summary: '新增接口', security: [['bearerAuth' => []]], tags: ['文章模块'])]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: ArticleCreateRequest::class))]
    #[OA\Response(response: 200, description: 'OK')]
    public function create(): Response
    {
        DB::startTrans();
        try {
            $request = $this->request->get();

            $v = new ArticleCreateRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $articleEntity = new ArticleEntity;
            $articleEntity->loadData($request);

            $articleBundleService = new ArticleBundleService;
            $insertId = $articleBundleService->save($articleEntity->toEntity());
            if ($insertId > 0) {
                DB::commit();

                return $this->success('新增数据成功');
            }

            throw new CustomException('新增数据失败');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('新增数据错误');
        }
    }

    #[OA\Get(path: '/article/show', summary: '获取详情接口', security: [['bearerAuth' => []]], tags: ['文章模块'])]
    #[OA\Parameter(name: 'id', description: 'ID', in: 'query', required: true, example: 1)]
    #[OA\Response(response: 200, description: 'OK', content: new OA\JsonContent(ref: ArticleResponse::class))]
    public function show(): Response
    {
        try {
            $id = intval($this->request->param('id', 0));

            $condition = [
                ['id', '=', $id],
            ];

            $articleBundleService = new ArticleBundleService;
            $article = $articleBundleService->getOne($condition);

            if (empty($article)) {
                throw new CustomException('数据不存在或状态异常');
            }

            $response = new ArticleResponse;
            $response->loadData($article);

            return $this->success($response->toArray());
        } catch (Throwable $e) {
            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('获取详情错误');
        }
    }

    #[OA\Put(path: '/article/update', summary: '更新接口', security: [['bearerAuth' => []]], tags: ['文章模块'])]
    #[OA\RequestBody(required: true, content: new OA\JsonContent(ref: ArticleUpdateRequest::class))]
    #[OA\Response(response: 200, description: 'OK')]
    public function update(): Response
    {
        DB::startTrans();
        try {
            $request = $this->request->get();

            $v = new ArticleUpdateRequest;
            if (! $v->check($request)) {
                throw new CustomException($v->getError());
            }

            $articleBundleService = new ArticleBundleService;
            $article = $articleBundleService->getById($request['id']);
            if (empty($article)) {
                throw new CustomException('数据不存在或状态异常');
            }

            $articleEntity = new ArticleEntity;
            $articleEntity->loadData($request);

            $articleBundleService->update($articleEntity->toEntity(), [
                ['id', '=', $request['id']],
            ]);

            DB::commit();

            return $this->success('更新数据成功');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('更新数据错误');
        }
    }

    #[OA\Delete(path: '/article/destroy', summary: '删除接口', security: [['bearerAuth' => []]], tags: ['文章模块'])]
    #[OA\Parameter(name: 'id', description: 'ID', in: 'query', required: true, example: 1)]
    #[OA\Response(response: 200, description: 'OK')]
    public function destroy(): Response
    {
        DB::startTrans();
        try {
            $id = intval($this->request->param('id', 0));

            $condition = [
                ['id', '=', $id],
            ];

            $articleBundleService = new ArticleBundleService;
            if ($articleBundleService->remove($condition)) {
                DB::commit();

                return $this->success('删除数据成功');
            }

            throw new CustomException('删除数据失败');
        } catch (Throwable $e) {
            DB::rollback();

            if ($e instanceof CustomException) {
                return $this->error($e->getMessage());
            }

            Log::error($e);

            return $this->error('删除数据错误');
        }
    }
}
